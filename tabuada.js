function continha (){
    tabuada = 1
    while (tabuada <= 10){
        console.log ('10 x ' + tabuada + ' = ' + tabuada * 10)
        tabuada++
    }
}
continha()

//tabuadinha suave, resumindo completamente: função continha, tabuada eh igual a 1, enquanto tabuada n for = a 10, vai sempre aumentar o numero da tabuada, até chegar no 10, enquanto isso no console.log vai-se fazendo toda a conta necessária para a realização de toda a tabuada